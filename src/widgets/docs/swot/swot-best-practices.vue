          <p class="swot-best-practices__mistake-text">
            One person's perspective is always limited. Involve two or three people with different
            views of the situation. A frontline employee often sees weaknesses that management
            misses.
          </p>
        </div>
        <div class="swot-best-practices__mistake">
          <strong class="swot-best-practices__mistake-title">
            Treating it as a one-time exercise
          </strong>
          <p class="swot-best-practices__mistake-text">
            A SWOT done once and never revisited goes stale fast. Markets change, teams change,
            competitors move. Revisit your analysis every six to twelve months, or whenever
            something significant shifts.
          </p>
        </div>
        <div class="swot-best-practices__mistake">
          <strong class="swot-best-practices__mistake-title"
            >Being too polite about weaknesses</strong
          >
          <p class="swot-best-practices__mistake-text">
            People naturally soften criticisms, especially in a group setting. An analysis full of
            gentle, cushioned weaknesses is not honest. The whole point of this quadrant is to
            surface uncomfortable truths so you can deal with them.
          </p>
        </div>
        <div class="swot-best-practices__mistake">
          <strong class="swot-best-practices__mistake-title"
            >Listing everything you can think of</strong
          >
          <p class="swot-best-practices__mistake-text">
            More is not better. A SWOT with 40 items per quadrant becomes impossible to act on.
            Prioritise ruthlessly. Five sharp, specific items are worth more than twenty vague ones.
          </p>
        </div>
      </div>
