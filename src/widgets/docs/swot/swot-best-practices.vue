      <p class="swot-best-practices__text">
        A healthy SWOT has roughly similar depth in each quadrant. If your strengths list has 15
        items and your weaknesses list has 2, the analysis is not balanced - it is optimistic
        fiction. Equally, a SWOT that only catalogues problems is not useful either.
      </p>
      <p class="swot-best-practices__text">
        Aim for honest coverage. If a quadrant feels thin, spend more time on it before moving on.
        Ask a colleague or someone outside the situation to challenge your blind spots.
      </p>
    </section>

    <section class="swot-best-practices__section">
      <h2 class="swot-best-practices__section-title">Look for connections across quadrants</h2>
      <p class="swot-best-practices__text">
        The real insight in a SWOT comes from reading across it, not from reading each quadrant in
        isolation. Once all four are filled, ask these questions:
      </p>
      <ul class="swot-best-practices__list">
        <li>
          Which strengths can you use to take advantage of an opportunity? These are your best bets
          for immediate action.
        </li>
        <li>
          Which weaknesses make a threat more dangerous? These are your most urgent risks to
          address.
        </li>
        <li>
          Which weaknesses are stopping you from reaching an opportunity? These tell you where to
          invest.
        </li>
        <li>
          Which strengths could protect you against a threat? These are your natural defences.
        </li>
      </ul>
    </section>

    <section class="swot-best-practices__section">
      <h2 class="swot-best-practices__section-title">Turn the analysis into action</h2>
      <p class="swot-best-practices__text">
        A SWOT that ends as a list on a board has not done its job. The purpose of the exercise is
        to inform decisions. After completing your analysis, take the most important insight from
        each quadrant and write a single sentence that answers: "So what do we do about this?"
      </p>
      <p class="swot-best-practices__text">
        You do not need to act on every item. Pick the three to five that matter most and decide on
        a concrete next step for each. The rest can stay as context.
      </p>
    </section>

